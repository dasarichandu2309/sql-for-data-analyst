CREATE DATABASE LIBRARYDB;
USE LIBRARYDB;

CREATE TABLE BOOKS(
BOOK_ID INT,
TITLE VARCHAR(30),
AUTHOR VARCHAR(30),
GENRE VARCHAR(30),
PUBLICATION_YEAR INT,
PRICE DECIMAL(8,2)
);

